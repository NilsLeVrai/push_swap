void	baby_sort(t_index **a_stack, t_index **b_stack, int len)
{
	t_index	*temp_a;
	t_index	*temp_b;

	temp_a = *a_stack;
	temp_b = *b_stack;
	if (temp_a || temp_b || len)
		return ;
	return ;
}